    # ymm1 = ymm1 + carry
    vpaddd  ymm1, ymm1, ymm3
    
    # ymm1 = ymm1 + ymm4 ~ single "carry-limb" from last step
    vpaddd  ymm1, ymm1, ymm4
    
    # ymm4 = carry for next step
    vpandn  ymm4, ymm8, ymm2

    vmovdqa ymmword ptr [rdi+rax], ymm1
    add     rax, 32
    cmp     rsi, rax
    jne     LOOP
    ret

