.global secsidh_internal_5119k234_carry32_avx2
secsidh_internal_5119k234_carry32_avx2:
    xor     eax, eax
    sal     rsi, 5
    vmovdqa ymm7, ymmword ptr [rip + .SHUFFLE_CARRY]
    vmovdqa ymm8, ymmword ptr [rip + .ZERO_1ST_MASK]
