    vpxor ymm4, ymm4, ymm4
LOOP:
    # ymm1 = next vector of limbs
    vmovdqa ymm1, ymmword ptr [rdi+rax]
    
    # ymm2 = carry
    # vpsrld  ymm2, ymm1, 26
    vpsrad  ymm2, ymm1, 26

